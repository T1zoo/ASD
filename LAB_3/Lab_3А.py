def find_numbers(limit):
    nums = set()  # Множество для хранения уникальных чисел

    # Перебираем степени 3, 5 и 7
    p3 = 0
    while 3 ** p3 <= limit:
        p5 = 0
        while (3 ** p3) * (5 ** p5) <= limit:
            p7 = 0
            while (3 ** p3) * (5 ** p5) * (7 ** p7) <= limit:
                # Вычисляем текущее число
                num = (3 ** p3) * (5 ** p5) * (7 ** p7)
                nums.add(num)  # Добавляем значение в множество
                p7 += 1  # Увеличиваем степень 7
            p5 += 1  # Увеличиваем степень 5
        p3 += 1  # Увеличиваем степень 3

    # Вывод отсортированных уникальных чисел
    print("Числа, удовлетворяющие условию:")
    for num in sorted(nums):
        print(num)


# Пример использования
limit = int(input("Введите число x: "))
find_numbers(limit)
